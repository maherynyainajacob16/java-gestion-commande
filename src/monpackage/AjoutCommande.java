    public void affiche2() {
        String[] rowNomPro;
        String getSql = "SELECT numProduit From produit";
        Connection Con1 = MyConnection.getconnection();
        try {
            Statement st = Con1.createStatement();
            ResultSet rs = st.executeQuery(getSql);
            int rownum = 0;
            while (rs.next()) {
                rownum++;
            }
            while (rs.previous())
                    ;
            rowNomPro = new String[rownum];
            int i = 0;
            while (rs.next()) {
                rowNomPro[i] = rs.getString(1);
                i++;
            }
            javax.swing.DefaultComboBoxModel<String> cmb1;
            cmb1 = new javax.swing.DefaultComboBoxModel(rowNomPro);
            jComboBox2_Produit.setModel(cmb1);
        } catch (SQLException ex) {
            Logger.getLogger(AjoutCommande.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
