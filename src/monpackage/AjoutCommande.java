    private void btn_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supprimerActionPerformed
        int[] sup = table_Commande.getSelectedRows();
        String[] numBD;
        int numLigne = 0;
        Connection con = MyConnection.getconnection();
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM commande");
            while (rs.next()) {
                numLigne++;
            }
            while (rs.previous())
                 ;
            numBD = new String[numLigne];

            int i = 0;
            while (rs.next()) {
                numBD[i] = rs.getString(1);
                i++;
            }
            for (int a : sup) {
                st.executeUpdate("DELETE FROM commande WHERE id = '" + numBD[a] + "' ");
            }

        } catch (SQLException ex) {
            Logger.getLogger(AjoutCommande.class.getName()).log(Level.SEVERE, null, ex);
        }
        afficherTable();
    }//GEN-LAST:event_btn_supprimerActionPerformed

    public void afficherTable() {
        int rowNum = 0;
        Connection con = MyConnection.getconnection();
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM commande");
            while (rs.next()) {
                rowNum++;
            }
            donnee = new String[rowNum][5];
            while (rs.previous())
                ;
            int i = 0;
            int j;
            java.text.SimpleDateFormat sk = new java.text.SimpleDateFormat("dd MMM yyyy");
            while (rs.next()) {
                j = 0;
                while (j < 5) {
                    donnee[i][j] = (j != 4) ? rs.getString(j + 1) : sk.format(rs.getDate(j + 1));
                    j++;
                }
                i++;
            }
            table_Commande.setModel(new javax.swing.table.DefaultTableModel(donnee, titres));
            jScrollPane1.setViewportView(table_Commande);
        } catch (SQLException ex) {
            Logger.getLogger(AjoutCommande.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

