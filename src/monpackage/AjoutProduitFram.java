    @SuppressWarnings("empty-statement")
    public void afficher(){
        String sqlGet;
        sqlGet = "SELECT * FROM produit";
        Connection Con = MyConnection.getconnection();
        try {
            Statement st = Con.createStatement();
            ResultSet rs = st.executeQuery(sqlGet);
            ResultSetMetaData Data;
            Data = rs.getMetaData();
            int ColNum = Data.getColumnCount();
            int RowNum = 0;   
            int i,j;
                while(rs.next())
                    RowNum++;
                while(rs.previous())
                    ;
                tableauData = new String[RowNum][ColNum];
                i = 0;
                      
                while(rs.next()){
                    j = 0;
                    while(j<ColNum){
                        tableauData [i][j] = rs.getString(j+1); 
                        j++;
                    }
                    i++;
                }  
                table_Produit.setModel(new javax.swing.table.DefaultTableModel(tableauData, titre));
            jScrollPane2.setViewportView(table_Produit);
                } catch (SQLException ex) {
            Logger.getLogger(AjoutProduitFram.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
