    private void btn_SuprimerProduitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuprimerProduitActionPerformed
        int[] sup = table_Produit.getSelectedRows();
        String[] numBD; 
        int numLign  = 0;
        Connection con = MyConnection.getconnection();
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM produit");
            while(rs.next()){
                numLign = numLign + 1;
            }
            while(rs.previous())
            ;
            numBD = new String[numLign];
           int i = 0;
            while(rs.next()){
                numBD[i] = rs.getString(1);
                i++;
            }
            for(int a:sup){
                st.executeUpdate("DELETE FROM produit WHERE numProduit = '"+numBD[a]+"' ");
                afficher();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AjoutProduitFram.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_SuprimerProduitActionPerformed

